package ch02.lecture.p05print;

public class C01Print {
	public static void main(String[] args) {
		//println : 파라미터 출력후 엔터까지 출력
		System.out.println("hello");//출력후
		System.out.println("World");//아래에서 월드 써짐 엔터까지 출력 된다는 뜻
		System.out.println(true);
		
		//파라미터 없으면 엔터만 출력
		System.out.println();
		System.out.println();
		
		System.out.println("세줄 출력됨??");
	}
}
